#include "../../cub.h"

void	free_buffer(char **buffer)
{
	int	i;

	i = 0;
	while (i < 1024)
	{
		if (buffer[i])
		{
			free(buffer[i]);
			buffer[i] = NULL;
		}
		i++;
	}
}
